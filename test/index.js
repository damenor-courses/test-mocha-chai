describe('Sumas', () => {
  
  // Suma de + con + tiene que ser +
  it('Suma de + con + tiene que ser +', () => {
    console.log(2 + 2)
    console.log(4)
  })
  
  // Suma de + mayor con - menor tiene que ser + 
  it('Suma de + mayor con - menor tiene que ser + ', () => {
    console.log(2 + (-1))
    console.log(1)
  })

})
